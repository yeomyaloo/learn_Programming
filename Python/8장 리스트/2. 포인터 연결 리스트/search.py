def search(self,data):
    #data와 값이 같은 노드를 검색
    cnt = 0
    ptr = self.head

    while prt is not None:
        if ptr.data == data:
            self.current = ptr
            return cnt

        cnt += 1
        ptr = ptr.next
    return -1

